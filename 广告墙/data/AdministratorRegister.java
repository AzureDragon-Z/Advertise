package ch4.data;

public class AdministratorRegister {
    String id;			//注册的id
    String password;	//注册的密码
    public void setID(String id) {
        this.id = id;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getID() {
        return id;
    }

    public String getPassword() {
        return password;
    }
}
